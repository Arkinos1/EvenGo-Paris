const TABLE: Record<Language, Dict> = {
  fr: FR,
  en: EN,
  es: ES,
  de: DE,
  zh: ZH,
};

export function languageFromTag(tag: string | null | undefined): Language {
  if (!tag) return 'fr';
  const normalized = tag.trim().toLowerCase();
  if (normalized.startsWith('fr')) return 'fr';
  if (normalized.startsWith('en')) return 'en';
  if (normalized.startsWith('es')) return 'es';
  if (normalized.startsWith('de')) return 'de';
  if (normalized.startsWith('zh')) return 'zh';
  return 'fr';
}

export function detectLanguage(): Language {
  if (typeof navigator !== 'undefined') {
    return languageFromTag(navigator.language);
  }

  if (typeof document !== 'undefined') {
    const htmlLang = document.documentElement?.lang;
    if (htmlLang && htmlLang.trim().length > 0) {
      return languageFromTag(htmlLang);
    }
  }

  return 'fr';
}

export function t(language: Language, key: string): string {
  return TABLE[language][key] ?? TABLE.fr[key] ?? key;
}
